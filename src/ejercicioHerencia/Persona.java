
package ejercicioHerencia;

public class Persona {
    private String Nombre;
    private int Edad;

    public Persona(String Nombre, int Edad) {
        this.Nombre = Nombre;
        this.Edad = Edad;
    }

    public String getNombre() {
        return Nombre;
    }

    public int getEdad() {
        return Edad;
    }
    
}
